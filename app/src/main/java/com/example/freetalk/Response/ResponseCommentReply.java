package com.example.freetalk.Response;


public class ResponseCommentReply {
    String replyUserName;
    String replyDays;
    String reply;
    String replyFavouriteCount;
    String replyTalkCount;

    public String getReplyUserName() {
        return replyUserName;
    }

    public void setReplyUserName(String replyUserName) {
        this.replyUserName = replyUserName;
    }

    public String getReplyDays() {
        return replyDays;
    }

    public void setReplyDays(String replyDays) {
        this.replyDays = replyDays;
    }

    public String getReply() {
        return reply;
    }

    public void setReply(String reply) {
        this.reply = reply;
    }

    public String getReplyFavouriteCount() {
        return replyFavouriteCount;
    }

    public void setReplyFavouriteCount(String replyFavouriteCount) {
        this.replyFavouriteCount = replyFavouriteCount;
    }

    public String getReplyTalkCount() {
        return replyTalkCount;
    }

    public void setReplyTalkCount(String replyTalkCount) {
        this.replyTalkCount = replyTalkCount;
    }
}
